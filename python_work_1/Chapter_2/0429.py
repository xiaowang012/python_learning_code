#coding=utf-8
first_name="wang"
last_name="li"
full_name=first_name +" " +last_name#合并字符串，用＋号来合并
print(full_name.title())#首字母大写
print(full_name.upper())#全部都大写
print(full_name.lower())#全部都小写
print("hello!"+full_name.title()+" "+"good morning")
message="Hello,"+full_name.title()+"!"
print(message)
print("\tpython")#\t是加制表符
print("my name is \nwangli")#\n是加换行符
#制表符和换行符可同时使用\n\t，表示换行并下一行加一个制表符

